    private void btnsubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsubmitActionPerformed
        // TODO add your handling code here:
        String fn = tfn.getText();
        String ln = tln.getText();
        String usn = tun.getText();
        String pass = new String(jpass.getPassword());
        String compas = new String(jcpass.getPassword());
//        
        int y = reg.confirmPassword(pass, compas);
        int z = reg.checkUsername(usn);

        if (!"".equals(fn) && !"".equals(ln) && !"".equals(usn) && !"".equals(pass) && !"".equals(compas)) {
            if (y == 1) {
                if (z == 0) {

                    int x = reg.register(fn, ln, usn, pass);
                    if (x == 1) {
                        JOptionPane.showMessageDialog(Reg_form, "Successfully Registered!");
                        this.clear_reg_txtfld();
                        Reg_form.setVisible(false);
                        Reg_form.setAlwaysOnTop(false);
                    }
                } else {
                    JOptionPane.showMessageDialog(Reg_form, "Username Already Exist!", "Warning", JOptionPane.WARNING_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(Reg_form, "Password Does Not Match!", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(Reg_form, "Please Fill out the fields!", "Error", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_btnsubmitActionPerformed

